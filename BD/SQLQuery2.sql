CREATE DATABASE EMPRESA;





DROP TABLE EMPREGADOS

USE EMPRESA;
CREATE TABLE EMPREGADOS
(
CODFUN INT IDENTITY(1,1) NOT NULL,
NOME VARCHAR(30)NOT NULL,
NUM_DEPEND TINYINT,
DATA_NASCIMENTO DATETIME NOT NULL,
COD_DEPTO INT,
COD_CARGO INT,
DATA_ADMISSAO DATETIME NOT NULL,
SALARIO DECIMAL(7,2) NOT NULL,
SINDICALIZADO VARCHAR(30) NOT NULL,
OBS VARCHAR(400) NULL,

CONSTRAINT PK_EMPREGADOS 
PRIMARY KEY (CODFUN)
);






DROP TABLE TABELADEP

USE EMPRESA;
CREATE TABLE TABELADEP
(
COD_DEPTO INT IDENTITY(1,1) NOT NULL,
DEPTO VARCHAR(30) NOT NULL,

CONSTRAINT PK_DEPTO
PRIMARY KEY (COD_DEPTO)
);












DROP TABLE DEPENDENTES

USE EMPRESA;
CREATE TABLE DEPENDENTES
(
CODDEP INT IDENTITY(1,1) NOT NULL,
CODFUN INT NOT NULL,
NOME VARCHAR(30) NOT NULL,
DATA_NASCIMENTO DATETIME NOT NULL,


CONSTRAINT PK_DEPENDENTES
PRIMARY KEY (CODDEP),


CONSTRAINT FK_DEPENDENTES_CODEMPREGADOS
FOREIGN KEY (CODFUN)
	REFERENCES EMPREGADOS (CODFUN)
);








DROP TABLE TABELACAR

USE EMPRESA;
CREATE TABLE TABELACAR
(
COD_CARGO INT IDENTITY(1,1) NOT NULL,
CARGO VARCHAR(30) NOT NULL,
SALARIO_INIC DECIMAL(7,2) NOT NULL,


CONSTRAINT PK_TABELACAR 
PRIMARY KEY (COD_CARGO)
)













